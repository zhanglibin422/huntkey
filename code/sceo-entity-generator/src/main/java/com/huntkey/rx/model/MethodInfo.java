package com.huntkey.rx.model;

public class MethodInfo {

	private String name;
	private String type;
	private String remark;
	private String id;

	private String edmmArithmeticDesc;

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String type) {
		this.id = id;
	}

	public String getEdmmArithmeticDesc() {
		return edmmArithmeticDesc;
	}

	public void setEdmmArithmeticDesc(String edmmArithmeticDesc) {
		this.edmmArithmeticDesc = edmmArithmeticDesc;
	}


}
